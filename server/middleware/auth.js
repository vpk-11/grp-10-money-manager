const jwt = require('jsonwebtoken');
const User = require('../models/User');

// Enhanced Authentication Middleware for AI Chatbot Integration
// Provides user context for personalized chatbot responses
const auth = async (req, res, next) => {
  try {
    const token = req.header('Authorization')?.replace('Bearer ', '');
    
    if (!token) {
      return res.status(401).json({ message: 'No token, authorization denied' });
    }

    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    const user = await User.findById(decoded.userId).select('-password');
    
    if (!user) {
      return res.status(401).json({ message: 'Token is not valid' });
    }

    // Attach full user object for chatbot personalization
    req.user = user;
    req.userId = user._id; // Additional user ID for chatbot context
    next();
  } catch (error) {
    console.error('Auth middleware error:', error);
    res.status(401).json({ message: 'Token is not valid' });
  }
};

module.exports = auth;